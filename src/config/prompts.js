export function getMatchingOptions(dynasty) {
  const matchingLandmarks = Object.entries(XIAN_CULTURAL_PROMPTS.xian_landmarks)
    .filter(([key, config]) => config.dynasty === dynasty)
    .map(([key, config]) => ({ id: key, ...config }))

  const matchingCostumes = Object.entries(XIAN_CULTURAL_PROMPTS.dynasty_costumes)
    .filter(([key, config]) => key === dynasty.toLowerCase())
    .map(([key, config]) => ({ id: key, ...config }))

  return {
    landmarks: matchingLandmarks,
    costumes: matchingCostumes
  }
}

